{
}

//=============================================================================
// デストラクタ
//=============================================================================
CRankingButton::~CRankingButton()
{
}

//=============================================================================
// 生成処理関数呼び出し
//=============================================================================
CRankingButton * CRankingButton::Create(D3DXVECTOR3 Position)
{
	//ランキングボタンのポインタ
	CRankingButton * pRankingButton = NULL;
	//ランキングボタンのポインタがNULLの場合
	if (pRankingButton == NULL)
	{
		//ランキングボタンのメモリ確保
		pRankingButton = new CRankingButton;
		//ランキングボタンのポインタがNULLではない場合
		if (pRankingButton != NULL)
		{
			//ランキングボタンの位置設定
			pRankingButton->SetPosition(Position);
			//ランキングボタンの初期化処理関数呼び出し
			pRankingButton->Init();
		}
	}
	//ランキングボタンのポインタを返す
	return pRankingButton;
}

//=============================================================================
// 初期化処理関数
//=============================================================================
HRESULT CRankingButton::Init(void)
{
	//ボタンの初期化処理関数呼び出し
	CButton::Init();
	return S_OK;
}

//=============================================================================
// 終了処理関数
//=============================================================================
void CRankingButton::Uninit(void)
{
	//ボタンの終了処理関数呼び出し
	CButton::Uninit();
}

//=============================================================================
// 更新処理関数
//=============================================================================
void CRankingButton::Update(void)
{
	//ボタンの更新処理関数呼び出し
	CButton::Update();
}

//=============================================================================
// 描画処理関数
//=============================================================================
void CRankingButton::Draw(void)
{
	//ボタンの描画処理関数呼び出し
	CButton::Draw();
}

//=============================================================================
// プレス処理関数
//=============================================================================
void CRankingButton::Press(void)
{
	//サウンドの取得
	CSound * pSound = CManager::GetSound();
	//もしサウンドのポインタがNULLじゃない場合
	if (pSound != NULL)
	{
		//決定音の再生
		pSound->PlaySoundA(CSound::SOUND_LABEL_SE_BUTTON_PUSH);
	}
	//ランキングモードに遷移する
	CManager::StartFade(CManager::MODE_RANKING);
}