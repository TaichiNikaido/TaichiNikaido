CNameMode * CNameMode::Create()
{
	//名前入力モードのポインタ
	CNameMode * pNameMode = NULL;
	//名前入力モードのポインタがNULLの場合
	if (pNameMode == NULL)
	{
		//名前入力モードのメモリ確保
		pNameMode = new CNameMode;
		//もし名前入力モードのポインタがNULLじゃない場合
		if (pNameMode != NULL)
		{
			//名前入力モードの初期化処理関数呼び出し
			pNameMode->Init();
		}
	}
	//名前入力モードのポインタを返す
	return pNameMode;
}

