//=============================================================================
// 生成処理関数
//=============================================================================
CVillage * CVillage::Create()
{
	//村のポインタ
	CVillage * pVillage = NULL;
	//もし村のポインタをNULLだった場合
	if (pVillage == NULL)
	{
		//村のメモリ確保
		pVillage = new CVillage;
		//もし村のポインタをNULLじゃない場合
		if (pVillage != NULL)
		{
			//村の初期化関数呼び出し
			pVillage->Init();
		}
	}
	//村のポインタを返す
	return pVillage;
}

