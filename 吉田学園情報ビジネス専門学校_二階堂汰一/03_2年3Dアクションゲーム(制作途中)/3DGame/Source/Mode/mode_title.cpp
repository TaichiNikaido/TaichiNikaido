//=============================================================================
// 生成処理関数
//=============================================================================
CTitleMode * CTitleMode::Create()
{
	//タイトルモードのポインタ
	CTitleMode * pTitleMode = nullptr;
	//もしタイトルモードのポインタがnullptrの場合
	if (pTitleMode == nullptr)
	{
		//タイトルモードのメモリ確保
		pTitleMode = new CTitleMode;
		//もしタイトルモードのポインタがnullptrではない場合
		if (pTitleMode != nullptr)
		{
			//初期化処理関数呼び出し
			pTitleMode->Init();
		}
	}
	//タイトルモードのポインタを返す	
	return pTitleMode;
}

//=============================================================================
// 初期化処理関数
//=============================================================================
HRESULT CTitleMode::Init(void)
{
	//サウンドの取得
	CSound * pSound = CManager::GetSound();
	//もしサウンドのポインタがnullptrではない場合
	if (pSound != nullptr)
	{
		//サウンドの停止
		pSound->StopSound();
		//タイトルのBGMを再生
		pSound->PlaySoundA(CSound::SOUND_LABEL_BGM_TITLE);
	}
	//初期全生成処理関数呼び出し
	InitCreateAll();
	return S_OK;
}

