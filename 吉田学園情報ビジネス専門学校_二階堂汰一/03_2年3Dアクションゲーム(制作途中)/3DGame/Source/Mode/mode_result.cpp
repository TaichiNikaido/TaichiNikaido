//=============================================================================
// 生成処理関数
//=============================================================================
CResultMode * CResultMode::Create()
{
	//リザルトモードのポインタ
	CResultMode * pResultMode = nullptr;
	//もしリザルトモードのポインタがnullptrの場合
	if (pResultMode == nullptr)
	{
		//リザルトモードのメモリ確保
		pResultMode = new CResultMode;
		//もしリザルトモードのポインタがnullptrではない場合
		if (pResultMode != nullptr)
		{
			//リザルトモードの初期化処理関数呼び出し
			pResultMode->Init();
		}
	}
	//リザルトモードのポインタを返す
	return pResultMode;
}

